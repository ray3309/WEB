package ex1;

public class GroupTourVO {
	private int    tourNum;
	private String tourTitle;
	private String tourWriter;
	private String tourImagePath;
	private String tourContent;
	private String tourDate;
		
	public int getTourNum() {
		return tourNum;
	}
	public void setTourNum(int tourNum) {
		this.tourNum = tourNum;
	}
	public String getTourTitle() {
		return tourTitle;
	}
	public void setTourTitle(String tourTitle) {
		this.tourTitle = tourTitle;
	}
	public String getTourWriter() {
		return tourWriter;
	}
	public void setTourWriter(String tourWriter) {
		this.tourWriter = tourWriter;
	}
	public String getTourImagePath() {
		return tourImagePath;
	}
	public void setTourImagePath(String tourImagePath) {
		this.tourImagePath = tourImagePath;
	}
	public String getTourContent() {
		return tourContent;
	}
	public void setTourContent(String tourContent) {
		this.tourContent = tourContent;
	}
	public String getTourDate() {
		return tourDate;
	}
	public void setTourDate(String tourDate) {
		this.tourDate = tourDate;
	}	
}
